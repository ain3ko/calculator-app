
class Calculator{
	
	
	static void main(string args[]){
	int a = 6;
	int b = 7;
	
	Console.WriteLine("Jumlah {0} + {1} = {2}", a, b, tambah(a,b);
	Console.WriteLine("Jumlah {0} - {1} = {2}", a, b, kurang(a,b);
	
	}
	
	static int tambah(int a, int b){
		return a+b;
	}
	static int kurang(int a, int b){
		return a-b;
	}
}